#include<stdio.h>
int main()
{
    int a=168;
    float b=123.456;
    float x=35.567,y;
/*
%+5d右对齐的方式输出5个字符宽度，不足五位前补空格(当5改成2时，仍然会输出正常的168)
%-5d左对齐的方式输出5个字符宽度，不足五位后补空格
%+6.2f输出6个字符宽度（其中2位小数）实际上，正负号占字符宽度，当6改为1后也会正常输出整数部分
%E输出科学计数法
%#强制按照格式输出
*/
    printf(" a = %5d\n",a);
    printf(" a = %-5d\n",a);
    printf(" b = %6.2f\n",b);
    printf(" b = %E\n",b);
    y=(int)(x*100+0.5)/100.0;
    printf(" y = %f\n",y);

    printf("%#o,%4o,%6lo\n",045,045,-1);
   
//045, 045,37777777777
/*
%#o 表示以八进制格式输出，并加上前缀"0"
参数是 045（这是一个八进制字面量，等于十进制的 37）
输出结果：045
*/
/*
%4o 表示以八进制格式输出，至少占4个字符宽度，右对齐
参数是 045（同样是八进制 45）
八进制 45 占用 2 个字符，因此前面会填充 2 个空格
输出结果： 45（前面有两个空格）
*/
/*
第三个格式说明符 %6o：

%6o 表示以八进制格式输出，至少占6个字符宽度，右对齐
参数是 -1（十进制的 -1）
在大多数系统中，-1 被表示为全 1 的补码形式
当以无符号八进制输出时，会显示为一长串的 1
由于指定了至少 6 个字符宽度，但实际输出可能会超过这个宽度（因为负数的补码表示）
*/
/*
%6lo 用于第三个参数 -1：l 修饰符表示长整型，
%o 将负整数解释为无符号长整型。在32位系统上，-1 的
无符号长整型值为 2^32 - 1 = 4294967295，八进制表示为 "37777777777"。宽度为6，但实际数字长度11位大于6，
因此忽略宽度限制，输出完整数字。
*/
 printf("%6lx\n",-1);
    printf("%#g\n", 10.0);   // 输出: 10.（浮点数强制显示小数点）
    printf("%#f\n", 10.0);   // 输出: 10.000000（强制显示小数点后的0）
    return 0;
}