#include<stdio.h>
int main()
{
    int a=168;
    float b=123.456;
    float x=35.567,y;
/*
%+5d右对齐的方式输出5个字符宽度，不足五位前补空格(当5改成2时，仍然会输出正常的168)
%-5d左对齐的方式输出5个字符宽度，不足五位后补空格
%+6.2f输出6个字符宽度（其中2位小数）实际上，正负号占字符宽度，当6改为1后也会正常输出整数部分
%E输出科学计数法
*/
    printf(" a = %5d\n",a);
    printf(" a = %-5d\n",a);
    printf(" b = %6.2f\n",b);
    printf(" b = %E\n",b);
    y=(int)(x*100+0.5)/100.0;
    printf(" y = %f\n",y);
    return 0;
}